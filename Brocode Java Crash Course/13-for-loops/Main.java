
public class Main {
    public static void main(String[] args) {

        // for loop = executes a block of code a limited number of times
        // for loop: declare index, specify condition, specify index in/decrement

        // count from 0 to 10
        for (int i = 0; i <= 10; i++) {
            System.out.println(i);
        }

        // count from 10 to 0
        for (int i = 10; i >= 0; i--) {
            System.out.println(i);
        }

        System.out.println("Happy new year!");
    }
}
